#!/usr/bin/env node
// gap-proposals.mjs — V4 M4: the gap-driven proposal pipeline. Nightly-callable.
//
// Mines the demand signals the space actually exposes for asks the engine
// cannot answer, applies the pre-registered rule (>= 5 recurrences within 7
// days), and — only then — emits a candidate R method skeleton (spec + tests +
// registry entry) into candidates/<slug>/ for PI review. NEVER auto-deploys;
// a candidate is a proposal document, not code on any execution path.
//
// Demand sources, in honesty order:
//   1. GET {ENGINE}/metrics  -> .events aggregate counters (public, no PII;
//      counters only — event names carry no query content by design).
//   2. Cloud Run logs for /api/research + unknown-method 400s via
//      `gcloud logging read` (content-bearing; gracefully recorded as
//      "unreadable" when the CLI/permission is absent — never fabricated).
// Every run appends a record to candidates/RUNLOG.md — "no proposals" is a
// first-class, recorded outcome.
//
// usage: node scripts/gap-proposals.mjs [--selftest] [--dry]
import { readFileSync, writeFileSync, mkdirSync, existsSync, appendFileSync } from "node:fs";
import { spawnSync } from "node:child_process";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const CAND = join(ROOT, "candidates");
const ENGINE = process.env.ENGINE_URL || "https://shssm-compute-b7ui3oxaqq-el.a.run.app";
const THRESHOLD = 5, WINDOW_DAYS = 7;   // pre-registered rule (V4 M4.1)

async function fetchMetrics() {
  try {
    const r = await fetch(ENGINE + "/metrics", { signal: AbortSignal.timeout(10000) });
    if (!r.ok) return { ok: false, reason: `HTTP ${r.status}` };
    return { ok: true, json: await r.json() };
  } catch (e) { return { ok: false, reason: e.message }; }
}

function readResearchLogs() {
  // content-bearing asks: /api/research questions + unknown-method rejections.
  const filter = 'resource.type="cloud_run_revision" AND resource.labels.service_name="shssm-compute" AND (jsonPayload.path="/api/research" OR textPayload:"unknown method" OR jsonPayload.error="unknown method")';
  const r = spawnSync("gcloud", ["logging", "read", filter, "--limit", "200", `--freshness=${WINDOW_DAYS}d`, "--format", "json"],
    { encoding: "utf8", timeout: 60000 });
  if (r.status !== 0) return { ok: false, reason: (r.stderr || "gcloud failed").trim().slice(0, 200), asks: [] };
  let rows; try { rows = JSON.parse(r.stdout || "[]"); } catch { return { ok: false, reason: "bad JSON from gcloud", asks: [] }; }
  const asks = [];
  for (const row of rows) {
    const jp = row.jsonPayload || {};
    const txt = jp.question || jp.q || jp.method || row.textPayload || "";
    const m = String(txt).match(/unknown method[: ]+"?([a-z0-9_]+)"?/i);
    if (m) asks.push({ kind: "unknown_method", ask: m[1] });
    else if (jp.path === "/api/research" && (jp.question || jp.q)) asks.push({ kind: "research_q", ask: String(jp.question || jp.q).slice(0, 140) });
  }
  return { ok: true, reason: null, asks };
}

export function recurrences(asks) {
  const by = new Map();
  for (const a of asks) {
    const k = a.ask.toLowerCase().trim();
    by.set(k, (by.get(k) || 0) + 1);
  }
  return [...by.entries()].map(([ask, n]) => ({ ask, n })).sort((a, b) => b.n - a.n);
}

export function proposalMD(slug, ask, n) {
  return `# CANDIDATE (PI review required — never auto-deploy): ${slug}

Demand: the ask "${ask}" recurred ${n}x within ${WINDOW_DAYS} days (rule: >=${THRESHOLD}).
Generated by scripts/gap-proposals.mjs on ${new Date().toISOString().slice(0, 10)} — a
proposal document only; nothing here is on any execution path.

## Spec (to be completed at review)
- What the method computes, on which dataset(s), with what params (validate() types).
- Stationarity posture: log-returns I(0) only, never levels.
- Published-package wrap (preferred) or labelled reimplementation?

## Tests (required before any registry entry)
- Anchor: the published number it must reproduce, with its FULL parameterisation.
- Pre-registered eval band + an evals/run-evals.mjs row (eval law: evals >= methods).

## Registry entry (draft — NOT applied)
\`\`\`js
// ${slug}: { script: "${slug}.R", params: { dataset: {type:"enum", values:["g20","g20_24"]} }, long_running: false }
\`\`\`

## Decision
- [ ] PI approve  - [ ] PI reject  - [ ] needs data
`;
}

export function runOnce({ metrics, logs, dry = false, now = new Date().toISOString() }) {
  const asks = logs.asks || [];
  const rec = recurrences(asks);
  const over = rec.filter((r) => r.n >= THRESHOLD);
  const emitted = [];
  for (const o of over) {
    const slug = o.ask.toLowerCase().replace(/[^a-z0-9]+/g, "_").replace(/^_+|_+$/g, "").slice(0, 40) || "unnamed_ask";
    const dir = join(CAND, slug);
    if (!dry) {
      mkdirSync(dir, { recursive: true });
      writeFileSync(join(dir, "PROPOSAL.md"), proposalMD(slug, o.ask, o.n));
    }
    emitted.push({ slug, n: o.n });
  }
  const ev = metrics.ok ? (metrics.json.events || metrics.json.metrics?.events || null) : null;
  const lines = [
    `\n## Run ${now}`,
    `- /metrics: ${metrics.ok ? "read OK" + (ev ? ` (events: ${Object.keys(ev).length} counters)` : " (no events block)") : "UNREADABLE: " + metrics.reason}`,
    `- research/unknown-method logs: ${logs.ok ? `read OK (${asks.length} asks in ${WINDOW_DAYS}d)` : "UNREADABLE: " + logs.reason}`,
    `- rule: >=${THRESHOLD} recurrences / ${WINDOW_DAYS}d -> ${over.length ? over.map(o => `${o.ask} (${o.n}x)`).join("; ") : "no ask met the threshold"}`,
    `- proposals emitted: ${emitted.length ? emitted.map(e => `candidates/${e.slug}/`).join(", ") : "NONE (recorded as a first-class outcome)"}`,
  ];
  if (!dry) { mkdirSync(CAND, { recursive: true }); appendFileSync(join(CAND, "RUNLOG.md"), lines.join("\n") + "\n"); }
  return { rec, over, emitted, record: lines.join("\n") };
}

function selftest() {
  let pass = 0, fail = 0;
  const ok = (c, n) => { console.log(`${c ? "PASS" : "FAIL"}  ${n}`); c ? pass++ : fail++; };
  const mk = (ask, n) => Array.from({ length: n }, () => ({ kind: "unknown_method", ask }));
  const r1 = runOnce({ metrics: { ok: false, reason: "stub" }, logs: { ok: true, asks: [...mk("hawkes_process", 6), ...mk("copula_tail", 2)] }, dry: true });
  ok(r1.over.length === 1 && r1.over[0].ask === "hawkes_process", "6x ask crosses the pre-registered threshold; 2x does not");
  ok(r1.emitted[0].slug === "hawkes_process", "slug derived from the ask");
  ok(/PI review required — never auto-deploy/.test(proposalMD("x", "x", 6)), "proposal is PI-gated by construction");
  ok(/log-returns I\(0\)/.test(proposalMD("x", "x", 6)), "stationarity invariant embedded in the spec template");
  ok(/eval law/.test(proposalMD("x", "x", 6)), "eval-law requirement embedded");
  const r2 = runOnce({ metrics: { ok: false, reason: "stub" }, logs: { ok: false, reason: "denied", asks: [] }, dry: true });
  ok(r2.emitted.length === 0 && /UNREADABLE: denied/.test(r2.record), "unreadable source recorded honestly, nothing fabricated");
  ok(/NONE \(recorded as a first-class outcome\)/.test(r2.record), "no-proposal outcome is first-class");
  console.log(`\n${pass}/${pass + fail} gap-proposals selftests passed`);
  process.exit(fail ? 1 : 0);
}

import { pathToFileURL } from "node:url";
if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  const mode = process.argv[2] || "";
  if (mode === "--selftest") selftest();
  else {
    const dry = mode === "--dry";
    const metrics = await fetchMetrics();
    const logs = readResearchLogs();
    const r = runOnce({ metrics, logs, dry });
    console.log(r.record);
  }
}
